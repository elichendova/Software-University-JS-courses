function calc(a, b, operator) {
switch (operator) {
    case "+":
    function add (a, b ) {
        return a + b;
     }
     return add(a,b);
     case "-":
     function substract (a, b ) {
         return a - b;
      }
      return substract(a,b);
      case "/":
      function devide (a, b ) {
          return a / b;
       }
       return devide(a,b);
       case "*":
       function multiply (a, b ) {
           return a * b;
        }
        return multiply(a,b);
}
}